 * Class EmailLogModel
 * 邮件模型
 */
class EmailLogModel extends BaseModel {
	protected $tableName = 'user_email_log';
    protected $tableName1 = 'verify';
    protected $pk = "id";

    public function addLog($uid,$data,$verify_data = array()){
        $data['user_id'] = $uid;
        $res  = $this->add($data);
        if($res){
            $per = C('DB_PREFIX');
            $table = "{$per}".$this->$tableName1;
            if(is_array($verify_data)){
                $verify_data['ukey'] = $uid;
                $this->table($table)->add($verify_data);
            }
        }
    }
}
